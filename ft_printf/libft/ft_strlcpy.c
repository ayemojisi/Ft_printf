#include "libft.h"

size_t	ft_strlcpy(char *d1, const char *d2, size_t n)
{
	size_t	i;

	if (!n)
		return (ft_strlen(d2));
	i = 0;
	while (d2[i] && (i < n - 1))
	{
		d1[i] = d2[i];
		i++;
	}
	d1[i] = 0;
	return (ft_strlen(d2));
}
