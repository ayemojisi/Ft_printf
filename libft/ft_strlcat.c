#include "libft.h"

size_t	ft_strlcat(char *d1, const char *d2, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (d1[i] && i < n)
		i++;
	while (d2[j] && (i + j + 1) < n)
	{
		d1[i + j] = d2[j];
		j++;
	}
	if (i < n)
		d1[i + j] = '\0';
	return (i + ft_strlen(d2));
}
