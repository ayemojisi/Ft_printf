#include "libft.h"

char	*ft_strrchr(const char *d1, int c)
{
	int		i;
	char	*s1;

	i = 0;
	s1 = 0;
	while (d1[i])
	{
		if (d1[i] == (char)c)
			s1 = (char *)(d1 + i);
		i++;
	}
	if (d1[i] == (char)c)
	s1 = (char *)(d1 + i);
	return (s1);
}
